package program;

public class datatype 
{
		public static void main(String[] args) 
		{
			int age = 18;
			System.out.println(age);
			char initial = 'D';
			String name = "Monika";
			System.out.println(initial);
			System.out.println(name);
			float f = 10.42006f;
			double d = 18.10420062;
			System.out.println(f);
			System.out.println(d);
		}
}
