package Day3;

public class string 
{
	public static void main(String[] args)
	{
		//string literal
		String s1 = "mona";
		//string object
		String s2 = new String ("mano");
		//array of characters
		char[] c1 = {'J','A','V','A'};
		//char to string
		String s3 = new String(c1);
				
		System.out.println(s1);
		System.out.println(s2);
		System.out.println(s3);	
		
	}
}
